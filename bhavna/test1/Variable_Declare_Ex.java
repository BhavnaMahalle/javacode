



package bhavna.test1;

public class Variable_Declare_Ex {

	public static void main(String[] args) {
	
		
		//declare variable without value (variable declaration)
    String studentname;
    int ageofstudent;
    byte studentid;
    int rollno ;
    boolean result;
    char gender;
    
    // declare variable with value ( variable initialization)
    
    studentname = "Bhavna Mahalle";
    ageofstudent = 29;
    studentid = 21;
    rollno = 3553;
    result = true ;
    gender = 'F';
    
    System.out.println("Name :" + studentname);
    System.out.println("Age:" + ageofstudent);
    System.out.println("Student id :" + studentid);
    System.out.println("Roll no :" + rollno);
    System.out.println("Result :" + result);
    System.out.println("Gender :" + gender);
      
    
	}

}
