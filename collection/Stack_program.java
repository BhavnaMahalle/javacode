package collection;

import java.util.Stack;

public class Stack_program {

	public static void main(String[] args) {
		Stack s1 = new Stack();
		s1.push("Bhavna");
		System.out.println("Push->" +s1);
		
		s1.peek();
		System.out.println("Peek->" +s1);
		
		s1.pop();
		System.out.println("pop->" +s1);
		
		

	}

}
