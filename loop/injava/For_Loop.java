package loop.injava;

public class For_Loop {

	public static void main(String[] args) {
		// lets print name 2 times
		
		for(int i=1;i<3;i++) {
			
			System.out.println("Bhavna Kohale");
			
		}
		
		
		//lets print equal and below 5 from 0
		for(int i=0 ;i<= 5 ; i++ ) {
			
			System.out.println("Bhavna Kohale");
			
		}
			
		

	}

}
